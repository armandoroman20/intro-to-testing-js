function sayHello (input) {
    if ( input === true) {
        return "Hello, World!";
    }  else if ( input === false ) {
        return "Hello, World!";
    } else if ( input === undefined ) {
        return "Hello, World!";
    } else {
        return "Hello, " + input + "!";
    }
}
